    public void Guardar(Usuario nuevoUsuario) {
        try {
            PreparedStatement actualizar = Ejercicio151.connection.prepareStatement("UPDATE usuarios SET login=?, contraseña=?");
            PreparedStatement insertar = Ejercicio151.connection.prepareStatement("INSERT INTO usuarios VALUES(null,?,?)");

            if (nuevoUsuario.Existe(nuevoUsuario.getLogin(), nuevoUsuario.getContraseña()) == true) {
                actualizar.setString(1, nuevoUsuario.getLogin());
                actualizar.setString(2, nuevoUsuario.getContraseña());
                actualizar.execute();
            } else {
                insertar.setString(1, nuevoUsuario.getLogin());
                insertar.setString(2, nuevoUsuario.getContraseña());
                insertar.execute();
            }

        } catch (SQLException ex) {
            Logger.getLogger(Ejercicio151.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

