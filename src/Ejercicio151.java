    class Usuario {

        String login;
        String contraseña;

        public Usuario(String login, String contraseña) {
            this.login = login;
            this.contraseña = contraseña;
        }

        public String getContraseña() {
            return contraseña;
        }

        public void setContraseña(String contraseña) {
            this.contraseña = contraseña;
        }

        public String getLogin() {
            return login;
        }

        public void setLogin(String login) {
            this.login = login;
        }

        public boolean Existe(String login, String contraseña) {
            this.login = login;
            this.contraseña = contraseña;
            boolean existe = false;

            try {
                PreparedStatement usuario = Ejercicio151.connection.prepareStatement("SELECT * FROM USUARIOS WHERE login=? AND contraseña=?");
                usuario.setString(1, login);
                usuario.setString(2, contraseña);
                usuario.executeQuery();

                ResultSet rs = usuario.getResultSet();
                if (rs.next()) {
                    existe = true;
                } else {
                    existe = false;
                }
            } catch (SQLException ex) {
                Logger.getLogger(Ejercicio151.class.getName()).log(Level.SEVERE, null, ex);
            }
            return existe;

        }
    }
